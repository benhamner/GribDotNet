        checkRoundTrip2 defaultProjection2 25.0<Latitude> 260.0<Longitude>
        checkRoundTrip2 defaultProjection2 25.0<Latitude> 250.0<Longitude>
        checkRoundTrip2 defaultProjection2 25.0<Latitude> 200.0<Longitude>
        checkRoundTrip2 defaultProjection2 25.0<Latitude> 190.0<Longitude>
        checkRoundTrip2 defaultProjection2 15.0<Latitude> 210.0<Longitude>
        checkRoundTrip2 defaultProjection2 15.0<Latitude> 280.0<Longitude>
