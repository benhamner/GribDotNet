module LambertConverterTests

open LambertConverter

open NUnit.Framework
open FsUnit

[<TestFixture>]
type LambertConverterTests() =
    let Precision = 0.00001
    let defaultProjection1 = {
        StandardParallel0 = 25.0<Latitude>
        ReferenceLatitude = 25.0<Latitude>
        ReferenceLongitude = 265.0<Longitude>
        EarthRadius = 6371200.0
    }
    let defaultProjection2 = {
        StandardParallel1 = 20.0<Latitude>
        StandardParallel2 = 40.0<Latitude>
        ReferenceLatitude = 25.0<Latitude>
        ReferenceLongitude = 265.0<Longitude>
        EarthRadius = 6371200.0
    }

    let checkRoundTrip1 projection latitude longitude =
        let (easting, northing) = LambertConverter.toLambert1 projection (latitude, longitude)
        let (rLat, rLong) = LambertConverter.fromLambert1 projection (easting, northing)
        rLat |> should (equalWithin Precision) latitude
        rLong |> should (equalWithin Precision) longitude

    let checkRoundTrip2 projection latitude longitude =
        let (easting, northing) = LambertConverter.toLambert2 projection (latitude, longitude)
        let (rLat, rLong) = LambertConverter.fromLambert2 projection (easting, northing)
        rLat |> should (equalWithin Precision) latitude
        rLong |> should (equalWithin Precision) longitude

    let checkRoundTripRho1 projection latitude =
        let n = LambertConverter.Inner1.calculateN projection
        let rho = LambertConverter.Inner1.calculateRho projection n latitude
        let rLatitude = LambertConverter.Inner1.inverseRho projection n rho
        rLatitude |> should (equalWithin Precision) latitude

    let checkRoundTripRho2 projection latitude =
        let n = LambertConverter.Inner2.calculateN projection
        let rho = LambertConverter.Inner2.calculateRho projection n latitude
        let rLatitude = LambertConverter.Inner2.inverseRho projection n rho
        rLatitude |> should (equalWithin Precision) latitude

    let checkLambert1 projection distanceTolerance angleTolerance (latitude, longitude, easting, northing) =
        let (trialEasting, trialNorthing) = LambertConverter.toLambert1 projection (latitude*1.0<Latitude>, longitude*1.0<Longitude>)
        let (trialLatitude, trialLongitude) = LambertConverter.fromLambert1 projection (easting,northing)
        let eastingDelta = trialEasting - easting
        let northingDelta = trialNorthing - northing
        let latitudeDelta = float trialLatitude - latitude
        let longitudeDelta = float trialLongitude - longitude
        abs(eastingDelta) |> should lessThan distanceTolerance 
        abs(northingDelta) |> should lessThan distanceTolerance 
        abs(latitudeDelta) |> should lessThan angleTolerance
        abs(longitudeDelta) |> should lessThan angleTolerance

